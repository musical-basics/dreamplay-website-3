#!/usr/bin/env node
/**
 * update-variant-map.mjs
 *
 * Updates src/app/(website-pages)/customize/variant-map.ts from a JSON blob.
 *
 * Usage (pipe):
 *   echo '<json>' | node scripts/update-variant-map.mjs
 *
 * Usage (inline arg):
 *   node scripts/update-variant-map.mjs '<json>'
 *
 * Expected JSON shape:
 * {
 *   "full":  { "DS5.5": { "Black": "...", "White": "..." }, "DS6.0": {...}, "DS6.5": {...} },
 *   "solo":  { "DS5.5": { "Black": "...", "White": "..." }, "DS6.0": {...}, "DS6.5": {...} },
 *   "reservation": { ... },   // optional
 *   "reserve50":   { ... }    // optional
 * }
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUTPUT = path.resolve(__dirname, '../src/app/(website-pages)/customize/variant-map.ts');

// --- Read input ---
let raw = process.argv[2]; // CLI arg takes priority

if (!raw) {
    // Try stdin
    raw = fs.readFileSync('/dev/stdin', 'utf8').trim();
}

if (!raw) {
    console.error('❌  No JSON provided. Pass as arg or pipe via stdin.');
    process.exit(1);
}

let input;
try {
    input = JSON.parse(raw);
} catch (e) {
    console.error('❌  Invalid JSON:', e.message);
    process.exit(1);
}

const TIERS = ['full', 'solo', 'reservation', 'reserve50'];
const SIZES = ['DS5.5', 'DS6.0', 'DS6.5'];
const COLORS = ['Black', 'White'];

// --- Merge with existing defaults (read current file for fallback values) ---
// Only full/solo have real variant IDs — reservation/reserve50 are always empty
let existing = {};
for (const tier of TIERS) {
    existing[tier] = {};
    for (const size of SIZES) {
        existing[tier][size] = { Black: '', White: '' };
    }
}
try {
    const currentContent = fs.readFileSync(OUTPUT, 'utf8');
    // Find patterns like: 'DS6.0': { 'Black': '12345', 'White': '67890' }
    for (const tier of ['full', 'solo']) {
        const tierBlock = currentContent.match(
            new RegExp(`${tier}:[^{]*\\{([^}]*(?:\\{[^}]*\\}[^}]*)*)\\}`, 's')
        )?.[1] ?? '';
        for (const size of SIZES) {
            const sizeBlock = tierBlock.match(
                new RegExp(`'${size.replace('.', '\\.')}':\\s*\\{([^}]*)\\}`)
            )?.[1] ?? '';
            const black = sizeBlock.match(/'Black':\s*'([^']*)'/)?.[1] ?? '';
            const white = sizeBlock.match(/'White':\s*'([^']*)'/)?.[1] ?? '';
            existing[tier][size] = { Black: black, White: white };
        }
    }
} catch {
    // File doesn't exist yet — start blank
}

// --- Build merged map ---
const merged = {};
for (const tier of TIERS) {
    merged[tier] = {};
    for (const size of SIZES) {
        merged[tier][size] = {};
        for (const color of COLORS) {
            merged[tier][size][color] =
                input[tier]?.[size]?.[color] ??
                existing[tier]?.[size]?.[color] ??
                '';
        }
    }
}

// --- Generate TS file ---
const tierDescriptions = {
    full: 'DreamPlay Premium Bundle',
    solo: 'Keyboard Only',
    reservation: 'Lock My Spot ($99)',
    reserve50: 'Reserve (50%)',
};

const lines = [
    `/**`,
    ` * Shopify Variant ID Map`,
    ` *`,
    ` * Maps tier × size × color to Shopify variant IDs.`,
    ` * AUTO-GENERATED by scripts/update-variant-map.mjs — do not edit by hand.`,
    ` * Last updated: ${new Date().toISOString()}`,
    ` *`,
    ` * To update: node scripts/update-variant-map.mjs '<json>'`,
    ` */`,
    `export const VARIANT_MAP: Record<string, Record<string, Record<string, string>>> = {`,
];

for (const tier of TIERS) {
    lines.push(`    ${tier}: { // ${tierDescriptions[tier]}`);
    for (const size of SIZES) {
        const black = merged[tier][size]['Black'];
        const white = merged[tier][size]['White'];
        lines.push(`        '${size}': { 'Black': '${black}', 'White': '${white}' },`);
    }
    lines.push(`    },`);
}

lines.push(`};`, ``);

fs.writeFileSync(OUTPUT, lines.join('\n'), 'utf8');
console.log(`✅  variant-map.ts updated at ${OUTPUT}`);
console.log(`\nCurrent map:`);
for (const tier of TIERS) {
    console.log(`\n  [${tier}]`);
    for (const size of SIZES) {
        console.log(`    ${size} → Black: ${merged[tier][size]['Black'] || '(empty)'}  |  White: ${merged[tier][size]['White'] || '(empty)'}`);
    }
}
